/**
 * Design Token Compiler
 * Converts JSON design tokens to CSS variables, SCSS, JavaScript, and JSON formats
 * Supports token reference resolution and light/dark mode generation
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

class TokenCompiler {
  constructor() {
    this.tokens = {};
    this.resolvedTokens = {};
  }

  /**
   * Load all token files from the tokens directory
   */
  loadTokens(tokensDir) {
    const tokenFiles = [
      "colors.json",
      "typography.json",
      "spacing.json",
      "shadows.json",
      "radii.json",
      "animations.json",
    ];

    tokenFiles.forEach((file) => {
      const filePath = path.join(tokensDir, file);
      if (fs.existsSync(filePath)) {
        const category = path.basename(file, ".json");
        this.tokens[category] = JSON.parse(fs.readFileSync(filePath, "utf8"));
      }
    });

    return this.tokens;
  }

  /**
   * Resolve token references like {primitive.purple.600} to actual values
   */
  resolveReference(value, tokenCategory = null) {
    if (typeof value !== "string") return value;

    const referencePattern = /\{([^}]+)\}/g;
    let resolved = value;

    const matches = value.match(referencePattern);
    if (matches) {
      matches.forEach((match) => {
        const path = match.slice(1, -1); // Remove { and }
        const parts = path.split(".");

        // Start from the appropriate token category
        let current;
        if (parts[0] === "primitive" && this.tokens.colors) {
          current = this.tokens.colors.primitive;
          parts.shift(); // Remove 'primitive' from path
        } else if (
          parts[0] === "scale" &&
          tokenCategory === "radii" &&
          this.tokens.radii
        ) {
          current = this.tokens.radii.scale;
          parts.shift(); // Remove 'scale' from path
        } else if (parts[0] === "scale" && this.tokens.spacing) {
          current = this.tokens.spacing.scale;
          parts.shift(); // Remove 'scale' from path
        } else if (parts[0] === "duration" && this.tokens.animations) {
          current = this.tokens.animations.duration;
          parts.shift(); // Remove 'duration' from path
        } else if (parts[0] === "easing" && this.tokens.animations) {
          current = this.tokens.animations.easing;
          parts.shift(); // Remove 'easing' from path
        } else {
          // Try to find in all tokens
          current = this.tokens;
        }

        for (const part of parts) {
          if (current && typeof current === "object" && part in current) {
            current = current[part];
          } else {
            console.warn(`Warning: Could not resolve reference ${path}`);
            return value;
          }
        }

        // If we found a value, resolve it recursively
        const foundValue =
          typeof current === "object" && "value" in current
            ? current.value
            : current;

        resolved = resolved.replace(
          match,
          this.resolveReference(foundValue, tokenCategory),
        );
      });
    }

    return resolved;
  }

  /**
   * Generate CSS custom properties from tokens
   */
  generateCSS() {
    let css = "/* ========================================\n";
    css += "   DESIGN TOKENS - AUTO-GENERATED\n";
    css += "   Do not edit this file directly\n";
    css += "   Generated from design-system/tokens/\n";
    css += "   ======================================== */\n\n";
    css += ":root {\n";

    // Colors - Primitive tokens
    if (this.tokens.colors && this.tokens.colors.primitive) {
      css += "  /* Primitive Color Tokens */\n";
      Object.entries(this.tokens.colors.primitive).forEach(
        ([colorName, shades]) => {
          Object.entries(shades).forEach(([shade, value]) => {
            css += `  --${colorName}-${shade}: ${value};\n`;
          });
        },
      );
      css += "\n";
    }

    // Colors - Semantic tokens (light mode)
    if (this.tokens.colors && this.tokens.colors.semantic) {
      css += "  /* Semantic Color Tokens - Light Mode */\n";
      css += this.generateSemanticColorCSS(
        this.tokens.colors.semantic,
        "light",
      );
      css += "\n";
    }

    // Typography
    if (this.tokens.typography) {
      css += "  /* Typography Tokens */\n";
      Object.entries(this.tokens.typography).forEach(([category, tokens]) => {
        Object.entries(tokens).forEach(([name, token]) => {
          const value = this.resolveReference(token.value || token);
          css += `  --font-${category.replace("Families", "Family").replace("Sizes", "Size").replace("Weights", "Weight").replace("Heights", "Height")}-${name}: ${value};\n`;
        });
      });
      css += "\n";
    }

    // Spacing
    if (this.tokens.spacing) {
      css += "  /* Spacing Tokens */\n";
      if (this.tokens.spacing.scale) {
        Object.entries(this.tokens.spacing.scale).forEach(([name, token]) => {
          const value = this.resolveReference(token.value || token);
          css += `  --spacing-${name}: ${value};\n`;
        });
      }
      if (this.tokens.spacing.semantic) {
        Object.entries(this.tokens.spacing.semantic).forEach(
          ([name, token]) => {
            if (typeof token === "object" && !token.value) {
              // Handle nested semantic tokens (like button-padding-x with sm/md/lg)
              Object.entries(token).forEach(([size, val]) => {
                if (size !== "description") {
                  const value = this.resolveReference(val);
                  css += `  --${name}-${size}: ${value};\n`;
                }
              });
            } else {
              const value = this.resolveReference(token.value || token);
              css += `  --${name}: ${value};\n`;
            }
          },
        );
      }
      css += "\n";
    }

    // Shadows
    if (this.tokens.shadows) {
      css += "  /* Shadow Tokens - Light Mode */\n";
      Object.entries(this.tokens.shadows).forEach(([category, tokens]) => {
        Object.entries(tokens).forEach(([name, token]) => {
          const value = token.light || token.value || token;
          css += `  --shadow-${category}-${name}: ${value};\n`;
        });
      });
      css += "\n";
    }

    // Border Radii
    if (this.tokens.radii) {
      css += "  /* Border Radius Tokens */\n";
      if (this.tokens.radii.scale) {
        Object.entries(this.tokens.radii.scale).forEach(([name, token]) => {
          const value = this.resolveReference(token.value || token);
          css += `  --radius-${name}: ${value};\n`;
        });
      }
      if (this.tokens.radii.semantic) {
        Object.entries(this.tokens.radii.semantic).forEach(([name, token]) => {
          const value = this.resolveReference(token.value || token, "radii");
          css += `  --radius-${name}: ${value};\n`;
        });
      }
      css += "\n";
    }

    // Animations
    if (this.tokens.animations) {
      css += "  /* Animation Tokens */\n";
      Object.entries(this.tokens.animations).forEach(([category, tokens]) => {
        Object.entries(tokens).forEach(([name, token]) => {
          if (typeof token === "object" && !token.value) {
            // Handle semantic animations with duration and easing
            Object.entries(token).forEach(([prop, val]) => {
              if (prop !== "description") {
                const value = this.resolveReference(val);
                css += `  --animation-${name}-${prop}: ${value};\n`;
              }
            });
          } else {
            const value = this.resolveReference(token.value || token);
            css += `  --animation-${category}-${name}: ${value};\n`;
          }
        });
      });
      css += "\n";
    }

    css += "}\n\n";

    // Dark mode
    css += "/* Dark Mode Overrides */\n";
    css += ".dark {\n";

    // Semantic colors - dark mode
    if (this.tokens.colors && this.tokens.colors.semantic) {
      css += "  /* Semantic Color Tokens - Dark Mode */\n";
      css += this.generateSemanticColorCSS(this.tokens.colors.semantic, "dark");
      css += "\n";
    }

    // Shadows - dark mode
    if (this.tokens.shadows) {
      css += "  /* Shadow Tokens - Dark Mode */\n";
      Object.entries(this.tokens.shadows).forEach(([category, tokens]) => {
        Object.entries(tokens).forEach(([name, token]) => {
          if (token.dark) {
            css += `  --shadow-${category}-${name}: ${token.dark};\n`;
          }
        });
      });
    }

    css += "}\n";

    return css;
  }

  /**
   * Generate semantic color CSS for a specific mode
   */
  generateSemanticColorCSS(semantic, mode) {
    let css = "";

    Object.entries(semantic).forEach(([category, tokens]) => {
      Object.entries(tokens).forEach(([name, token]) => {
        if (typeof token === "object") {
          // Handle tokens with light/dark variants
          if (token[mode]) {
            const value = this.resolveReference(token[mode]);
            css += `  --color-${category}-${name}: ${value};\n`;
          } else if (token.value) {
            // Handle tokens with a single value
            const value = this.resolveReference(token.value);
            css += `  --color-${category}-${name}: ${value};\n`;
          } else {
            // Handle nested tokens (like status colors with value, light, dark)
            Object.entries(token).forEach(([subName, subToken]) => {
              if (subName !== "description" && typeof subToken === "string") {
                const value = this.resolveReference(subToken);
                css += `  --color-${category}-${name}-${subName}: ${value};\n`;
              }
            });
          }
        }
      });
    });

    return css;
  }

  /**
   * Generate SCSS variables
   */
  generateSCSS() {
    let scss = "// Design Tokens - SCSS Variables\n";
    scss += "// Auto-generated - do not edit directly\n\n";

    // Simply convert CSS custom properties to SCSS variables
    const css = this.generateCSS();
    const lines = css.split("\n");

    lines.forEach((line) => {
      const match = line.match(/^\s*--([^:]+):\s*([^;]+);/);
      if (match) {
        const [, name, value] = match;
        scss += `$${name}: ${value};\n`;
      }
    });

    return scss;
  }

  /**
   * Generate JavaScript/TypeScript exports
   */
  generateJS() {
    let js = "/**\n";
    js += " * Design Tokens - JavaScript/TypeScript\n";
    js += " * Auto-generated - do not edit directly\n";
    js += " */\n\n";

    js += "export const tokens = {\n";

    Object.entries(this.tokens).forEach(([category, data]) => {
      js += `  ${category}: ${JSON.stringify(data, null, 2).replace(/\n/g, "\n  ")},\n`;
    });

    js += "};\n\n";
    js += "export default tokens;\n";

    return js;
  }

  /**
   * Generate JSON output
   */
  generateJSON() {
    return JSON.stringify(this.tokens, null, 2);
  }

  /**
   * Compile all tokens and write output files
   */
  compile(tokensDir, outputDir) {
    console.log("Loading tokens...");
    this.loadTokens(tokensDir);

    console.log("Generating CSS...");
    const css = this.generateCSS();
    fs.writeFileSync(path.join(outputDir, "tokens.css"), css);

    console.log("Generating SCSS...");
    const scss = this.generateSCSS();
    fs.writeFileSync(path.join(outputDir, "tokens.scss"), scss);

    console.log("Generating JavaScript...");
    const js = this.generateJS();
    fs.writeFileSync(path.join(outputDir, "tokens.js"), js);

    console.log("Generating JSON...");
    const json = this.generateJSON();
    fs.writeFileSync(path.join(outputDir, "tokens.json"), json);

    console.log("✓ Token compilation complete!");
    console.log(`  Output directory: ${outputDir}`);
  }
}

// CLI execution
const isMainModule =
  process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];

if (isMainModule) {
  const tokensDir = path.join(__dirname, "../tokens");
  const outputDir = path.join(__dirname, "../output");

  // Create output directory if it doesn't exist
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  const compiler = new TokenCompiler();
  compiler.compile(tokensDir, outputDir);
}

export default TokenCompiler;
